/// \file simple_geom_SteppingAction.cc
/// \brief Implementation of the simple_geom_SteppingAction class

#include "simple_geom_SteppingAction.hh"
#include "simple_geom_EventAction.hh"
#include "simple_geom_DetectorConstruction.hh"

#include "G4Step.hh"
#include "G4Event.hh"
#include "G4RunManager.hh"
#include "G4LogicalVolume.hh"

//....oooOO0OOooo........oooOO0OOooo........oooOO0OOooo........oooOO0OOooo......

simple_geom_SteppingAction::simple_geom_SteppingAction(simple_geom_EventAction* eventAction)
: G4UserSteppingAction(),
  fEventAction(eventAction),
  fScoringVolume(0)
{}

//....oooOO0OOooo........oooOO0OOooo........oooOO0OOooo........oooOO0OOooo......

simple_geom_SteppingAction::~simple_geom_SteppingAction()
{}

//....oooOO0OOooo........oooOO0OOooo........oooOO0OOooo........oooOO0OOooo......

void simple_geom_SteppingAction::UserSteppingAction(const G4Step* step)
{
  if (!fScoringVolume) { 
    const simple_geom_DetectorConstruction* detectorConstruction
      = static_cast<const simple_geom_DetectorConstruction*>
        (G4RunManager::GetRunManager()->GetUserDetectorConstruction());
    fScoringVolume = detectorConstruction->GetScoringVolume();   
  }

  // get volume of the current step
  G4LogicalVolume* volume 
    = step->GetPreStepPoint()->GetTouchableHandle()
      ->GetVolume()->GetLogicalVolume();
      
  // check if we are in scoring volume
  if (volume != fScoringVolume) return;

  // collect energy deposited in this step
  G4double edepStep = step->GetTotalEnergyDeposit();
  fEventAction->AddEdep(edepStep);  
}

